#listado 
Equipos = ["Licey","Aguilas","Escogidos","Toros","Estrellas","Gigantes"]
print(Equipos[3])# forma de entrar en un elemento del listado
Equipos[3] ="San Diego"# forma de cambiar un elemento en listado por uno nuevo.
print(Equipos)# si usamos los numero en negativo se dirige al final del elemento ejemplo -1 vendria siendo el ultimo equipo
print(Equipos[1:3])# forma de tomar un conjunto de elemento dentro del listado
print(Equipos[1:]) # este hace un recorido desde el primer que selecciong hasta el final
print(Equipos[:3])